Lines.getWinningLines = function(combination, wildId){
    var lines = [];

    for (var i = 0; i < Lines.LINES_MAP.length; i++){
        if (checkLine(Lines.LINES_MAP[i], combination)){
            lines.push(Lines.LINES_MAP[i]);
        }
    }

    function checkLine(line, combination){
        var id, i, pid;
        for (i = 0; i < line.length; i++){
            id = combination[i][line[i]];
            if (id != wildId){
                break;
            }
        }
        for (i = 0; i < line.length; i++){
            pid = combination[i][line[i]];
            if (pid != id && pid != wildId){
                return false;
            }
        }
        return true;
    }
    return lines;
};

